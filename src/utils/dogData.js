export const getRandomItems = (array, count) => {
  const shuffled = [...array].sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

export const getRandomDogCards = (count = 18) => {
  const cardPool = [];
  
  dogBreeds.forEach(breed => {
    breed.facts.forEach((fact, factIndex) => {
      cardPool.push({
        id: `${breed.id}-${factIndex}`,
        title: breed.title,
        fact: fact,
        description: breed.descriptions[factIndex % breed.descriptions.length],
        image: 'https://images.dog.ceo/breeds/retriever-golden/n02099601_1344.jpg'
      });
    });
  });
  
  return getRandomItems(cardPool, count);
}; 